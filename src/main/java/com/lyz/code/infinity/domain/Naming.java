package com.lyz.code.infinity.domain;

public class Naming {
	protected String domain;
	protected String subdomain;
	protected String childdomain;
	protected long namingid;
public String getDomain() {
	return domain;
}
public void setDomain(String domain) {
	this.domain = domain;
}
public String getSubdomain() {
	return subdomain;
}
public void setSubdomain(String subdomain) {
	this.subdomain = subdomain;
}
public String getChilddomain() {
	return childdomain;
}
public void setChilddomain(String childdomain) {
	this.childdomain = childdomain;
}
public long getNamingid() {
	return namingid;
}
public void setNamingid(long namingid) {
	this.namingid = namingid;
}

}
